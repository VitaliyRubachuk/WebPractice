var myObject =
 {
    String: "Hello",
    Number: 42,
    Boolean: true,
    Undefined: undefined,
    Null: null
};


console.log("String: " + myObject.String);
console.log("Number: " + myObject.Number);
console.log("Boolean: " + myObject.Boolean);
console.log("Undefined: " + myObject.Undefined);
console.log("Null: " + myObject.Null);